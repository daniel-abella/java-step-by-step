package cap03;

public class Operadores {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		//Aritméticos

		/*
		 /     /=
		 *     *=
		 +     +=
		 -     -=
		 %
		 ^
		 ++
		 --
		 */
		int sete = 7;

		System.out.println(sete++);
		System.out.println(++sete);

		//Lógicos

		/*
		 &
		 &&
		 |
		 ||
		 >
		 <
		 =>
		 <=
		 ==
		 !=
		 !
		 */

		//Bits

		/*
		 >>>
		 >>
		 ~
		 */

		System.out.println(~-1);

		System.out.println(10>>1);
	}

}
